    Emp AddEmpl(){
        String id = txtid.getText();
        String fn = txtfname.getText();
        String ln = txtlname.getText();
        String sex = cbogender.getSelectedItem().toString();
        String dob = txtdob.getText();
        String pos = this.cboposition.getSelectedItem().toString();
        String start = this.txtstart.getText();
        String sal = this.txtsalary.getText();
        String add = this.txtaddrest.getText();
        String phon = this.txtphone.getText();
        
        if(id.length()==0)Msg("Input Emp ID ",txtid);
        else if(fn.length()==0)Msg("Input first Name ",txtfname);
        else if(ln.length()==0)Msg("Input last name ",txtlname);
        else if(dob.length()==0)Msg("Date of Birsth",txtdob); 
        else if(start.length()==0)Msg("Input Start Working ",txtstart);
        else if(sal.length()==0)Msg("Input Salary",txtsalary);
        else if(add.length()==0) Msg("Input Address ",this.txtaddrest);
        else if(phon.length()==0)Msg("Input Phone number ",txtphone);
        else{
                try{
                  double salary = Double.parseDouble(sal);
                  return (new Emp(pos,start,phon,salary,new Person(id,fn,ln,sex,dob,add))); 
                  
                }catch(NumberFormatException exp){
                  Msg("Input Only number ",txtsalary);
                txtsalary.selectAll();
                
                } // end catch
                
                }// end if else
        
        return null;
    }
    
