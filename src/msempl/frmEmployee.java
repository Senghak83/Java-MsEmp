// show data in JTable
    private void ShowData(ArrayList<Emp> list , JTable tab){
        // class all data in table
        int rc = tab.getRowCount();
        for(int i = rc-1 ; i>=0 ; i--)
            model.removeRow(i);
        // add data to table
        for(int i = 0 ; i<list.size(); i++){
            
            Emp e = list.get(i);
            model.addRow(new Object[]{i+1,e.getID(),e.getFullName(),e.getSex(),
            e.getDob(),e.getPosition(),e.getStartWork(),e.getSalary()
                    ,e.getPhoneNumber(),e.getAddress()});
        }
    }

