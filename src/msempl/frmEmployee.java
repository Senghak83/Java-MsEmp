    private void btdeletActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btdeletActionPerformed
        
        int rs = t.getSelectedRow();
        if(rs!=-1){
        String name = "\n " + t.getValueAt(rs, 2);
        // show icon on confirm dialog 
        ImageIcon icon = new ImageIcon("src/icon/d.png");    
        int y = JOptionPane.showConfirmDialog(null,"Do you wat to Delet this row"+name,"Delet Recor",
                JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE,icon);
        if(y == JOptionPane.YES_OPTION){
            db.RemoveEmp(0);
            this.ShowData(db.getAllEmp(), t);
            btupdate.setEnabled(false);
            btdelet.setEnabled(false);
            btadd.setEnabled(true);
           }
        }
    }//GEN-LAST:event_btdeletActionPerformed

